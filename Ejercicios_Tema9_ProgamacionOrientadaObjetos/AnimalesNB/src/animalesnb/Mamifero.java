

package animalesnb;


public class Mamifero extends Animal {
    private String habitat;
    private String tipo;
    
    public Mamifero(){}

    public String getHabitat() {
        return habitat;
    }

    public void setHabitat(String habitat) {
        this.habitat = habitat;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
}
